class Ingredient {
  final String name;
  final String category; // e.g., 'Protein', 'Carbs', 'Vegetables', etc.
  final double calories;
  final double protein;
  final double carbs;
  final double fats;
  final String servingSize;
  final String servingUnit;
  final List<String> specificCategories;
  const Ingredient({
    required this.name,
    required this.category,
    required this.calories,
    required this.protein,
    required this.carbs,
    required this.fats,
    required this.servingSize,
    required this.servingUnit,
    required this.specificCategories,
  });

  // Helper method to calculate nutrients based on quantity
  Map<String, double> calculateNutrients(double quantity) {
    final multiplier = quantity / double.parse(servingSize);
    return {
      'calories': calories * multiplier,
      'protein': protein * multiplier,
      'carbs': carbs * multiplier,
      'fats': fats * multiplier,
    };
  }

  

  

}

