  static List<Ingredient> getIngredientsByCategory(BuildContext context, String category) {
    return getIngredients(context)
        .where((ingredient) => ingredient.category == category)
        .toList();
  }

  static List<Ingredient> searchIngredients(BuildContext context, String query) {
    return getIngredients(context)
        .where((ingredient) => ingredient.name.toLowerCase().contains(query.toLowerCase()))
            .toList();
  }

  // Add this method to filter ingredients by specific category
  static List<Ingredient> getIngredientsBySpecificCategory(BuildContext context, String specificCategory) {
    if (specificCategory == 'All') {
      return getIngredients(context);
    }
    return getIngredients(context)
        .where((ingredient) => ingredient.specificCategories.contains(specificCategory))
        .toList();
  }

  static List<Ingredient> getPaginatedIngredients(BuildContext context, String query, int page, int pageSize) {
    final allIngredients = getIngredients(context);
    final filteredIngredients = query.isEmpty
        ? allIngredients
        : allIngredients
            .where((ingredient) =>
                ingredient.name.toLowerCase().contains(query.toLowerCase()))
            .toList();

    final startIndex = page * pageSize;
    if (startIndex >= filteredIngredients.length) {
      return [];
    }

    final endIndex = (startIndex + pageSize) > filteredIngredients.length
        ? filteredIngredients.length
        : startIndex + pageSize;

    return filteredIngredients.sublist(startIndex, endIndex);
  }
